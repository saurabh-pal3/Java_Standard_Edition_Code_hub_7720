package com.Opeater;

import java.util.Arrays;

public class ForEach1 {

	public static void main(String[] args) {
	     int [] arry = {11,10,4,4,5,1};
	     Arrays.sort(arry);
	     for(int v:arry)
	     {
	    	 System.out.println(v);
	     }
	     String [] a= {"saurabh","Pal","Ram","Gunvant","Apple"};
	     Arrays.sort(a);
	     for(String s:a)
	     {
	    	 System.out.println(s);
	     }
		
		}

	}


